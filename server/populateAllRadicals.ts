async function populateAllRadicals() {
  try {
    console.log('Starting comprehensive radical population...');
    
    // Get all characters without radical_index
    const allChars = await db.select().from(chineseCharacters).where(isNull(chineseCharacters.radicalIndex));
    console.log(`Found ${allChars.length} characters without radical_index`);
    
    let updated = 0;
    let failed = 0;
    
    for (const char of allChars) {
      // Try to find radical mapping
      const radicalChar = expandedRadicalMap[char.simplified];
      
      if (radicalChar) {
        // Find the radical in the database
        const [radical] = await db.select().from(radicals).where(eq(radicals.simplified, radicalChar));
        
        if (radical) {
          await db.update(chineseCharacters)
            .set({ radicalIndex: radical.index })
            .where(eq(chineseCharacters.index, char.index));
          updated++;
        } else {
          failed++;
        }
      } else {
        // For unmapped characters, try to find a likely radical by decomposition
        // Default to finding a radical that appears in the character components
        // This is a fallback for placeholder characters
        failed++;
      }
    }
    
    console.log(`✓ Updated ${updated} characters`);
    console.log(`✗ Could not map ${failed} characters`);
    
    const finalCount = await db.select().from(chineseCharacters).where(({ radicalIndex }) => radicalIndex !== null);
    console.log(`Total characters with radical_index: ${finalCount.length}`);
    
    process.exit(0);
  } catch (error) {
    console.error('Error:', error);
    process.exit(1);
  }
}

populateAllRadicals();
